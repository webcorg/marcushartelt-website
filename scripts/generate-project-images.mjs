import { readdir, stat, mkdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const imagesRoot = path.join(projectRoot, "public", "images", "projects");
const outDir = path.join(projectRoot, "src", "data", "generated");
const outFile = path.join(outDir, "projectImages.ts");

const allowedExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);

function toPublicPath(filePath) {
  const relative = path.relative(path.join(projectRoot, "public"), filePath);
  return "/" + relative.split(path.sep).join("/");
}

function naturalSort(a, b) {
  return a.localeCompare(b, undefined, {
    numeric: true,
    sensitivity: "base",
  });
}

async function readProjectImages() {
  const result = {};

  if (!existsSync(imagesRoot)) {
    return result;
  }

  const projectFolders = await readdir(imagesRoot);

  for (const folderName of projectFolders.sort(naturalSort)) {
    const folderPath = path.join(imagesRoot, folderName);
    const folderStat = await stat(folderPath);

    if (!folderStat.isDirectory()) {
      continue;
    }

    const files = await readdir(folderPath);
    const imageFiles = [];

    for (const fileName of files.sort(naturalSort)) {
      const filePath = path.join(folderPath, fileName);
      const fileStat = await stat(filePath);
      const ext = path.extname(fileName).toLowerCase();

      if (!fileStat.isFile()) {
        continue;
      }

      if (!allowedExtensions.has(ext)) {
        continue;
      }

      if (fileName.startsWith(".")) {
        continue;
      }

      imageFiles.push(toPublicPath(filePath));
    }

    if (imageFiles.length > 0) {
      result[folderName] = imageFiles;
    }
  }

  return result;
}

const projectImages = await readProjectImages();

await mkdir(outDir, { recursive: true });

const content = `// AUTO-GENERATED by scripts/generate-project-images.mjs
// Do not edit manually.

export const projectImages: Record<string, string[]> = ${JSON.stringify(projectImages, null, 2)};
`;

await writeFile(outFile, content, "utf8");

const projectCount = Object.keys(projectImages).length;
const imageCount = Object.values(projectImages).reduce((sum, images) => sum + images.length, 0);

console.log(`Generated src/data/generated/projectImages.ts`);
console.log(`${projectCount} project folders`);
console.log(`${imageCount} images`);
